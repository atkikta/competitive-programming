int main(){
    using namespace std;
    
    int n, a, b;
    cin >> n >> a >> b;
    vector<int> A(n);
    cin >> A;

    int left = 0;
    int right = 1000000001;
    while (right-left>1){
        int mid = (left+right)/2;
        long long min_add_count=0;
        long long max_sub_count=0;
        for(int i=0; i<n; i++){
            if(A[i]<mid){
                min_add_count += (mid - A[i] + a -1)/a ;
            }
            if(A[i]>mid){
                max_sub_count += (A[i] - mid)/b;
            }
        }
        if(min_add_count > max_sub_count){
            right = mid;
        }else{
            left = mid;
        }
    }
    cout << left << endl;
    
    
    return 0;
}