    double dfs(int a, int b, int c){
        // System.out.println(String.format("%d %d %d", a, b, c));
        if(a>=100||b>=100||c>=100){
            return 0.0;
        }
        if(dp[a][b][c]>=0){
            return dp[a][b][c];
        }
        double ret = 0;
        ret += (dfs(a+1, b, c)+1) * (a*1.0/(a+b+c));
        ret += (dfs(a, b+1, c)+1) * (b*1.0/(a+b+c));
        ret += (dfs(a, b, c+1)+1) * (c*1.0/(a+b+c));
        dp[a][b][c] = ret;
        // System.out.println(String.format("%d %d %d %f", a, b, c, ret));
        return ret;
    }
